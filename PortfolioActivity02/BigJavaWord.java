public class BigJavaWord {
	public static void main(String[] args) {
		System.out.println("     J        A     V       V     A");
		System.out.println("     J       A A     V     V     A A");
		System.out.println("     J      A   A     V   V     A   A");
		System.out.println("J    J     AAAAAAA     V V     AAAAAAA");
		System.out.println("JJJJJJ    A       A     V     A       A");
	}
}